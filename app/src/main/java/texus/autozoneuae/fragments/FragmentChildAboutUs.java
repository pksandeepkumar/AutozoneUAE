package texus.autozoneuae.fragments;

import android.os.Bundle;
import android.support.annotation.Nullable;
import android.support.v4.app.Fragment;
import android.text.Html;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.TextView;

import texus.autozoneuae.R;
import texus.autozoneuae.utility.Utility;

public class FragmentChildAboutUs extends Fragment {

    TextView tvContent;

    @Nullable
    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState) {
        View view = inflater.inflate(
                R.layout.fragment_child_about_us, container, false);
        tvContent = (TextView) view.findViewById(R.id.tvContent);
        String aboutUsString = Utility.readFromAssets("content_about.txt",getActivity());

        tvContent.setText(Html.fromHtml(aboutUsString));

        return view;
    }


}
